import java.util.ArrayList;

public abstract class User {

    public ArrayList<Internship> searchInternships(ArrayList<Internship> internships) {
        return internships;
    }

    public ArrayList<Internship> searchInternships(ArrayList<Internship> internships, String keywords) {
        return internships;
    }

    public ArrayList<Internship> searchInternships(ArrayList<Internship> internships, Internship filters) {
        return internships;
    }

}
